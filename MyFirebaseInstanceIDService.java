package com.codetho.photocollage.fcm;

import android.os.AsyncTask;
import android.os.Build;
import android.util.Log;

import com.codetho.photocollage.config.ALog;
import com.google.firebase.iid.FirebaseInstanceId;
import com.google.firebase.iid.FirebaseInstanceIdService;
import com.google.firebase.messaging.FirebaseMessaging;

import dauroi.photoeditor.api.StoreItemService;
import dauroi.photoeditor.api.response.RegisterPushResponse;
import dauroi.photoeditor.utils.ProfileCache;


public class MyFirebaseInstanceIDService extends FirebaseInstanceIdService {

    private static final String TAG = "MyFirebaseIIDService";

    /**
     * Called if InstanceID token is updated. This may occur if the security of
     * the previous token had been compromised. Note that this is called when the InstanceID token
     * is initially generated so this is where you would retrieve the token.
     */
    // [START refresh_token]
    @Override
    public void onTokenRefresh() {
        // Get updated InstanceID token.
        String refreshedToken = FirebaseInstanceId.getInstance().getToken();
        Log.d(TAG, "Refreshed token: " + refreshedToken);

        // TODO: Implement this method to send any registration to your app's servers.
        sendRegistrationToServer(refreshedToken);
        FirebaseMessaging.getInstance().subscribeToTopic("news");
    }
    // [END refresh_token]

    /**
     * Persist token to third-party servers.
     *
     * Modify this method to associate the user's FCM InstanceID token with any server-side account
     * maintained by your application.
     *
     * @param token The new token.
     */
    private void sendRegistrationToServer(final String token) {
        // Add custom implementation, as needed.
        AsyncTask<Void, Void, Void> task = new AsyncTask<Void, Void, Void>() {
            @Override
            protected Void doInBackground(Void... params) {
                // Add custom implementation, as needed.
                try {
                    ProfileCache.saveGcmToken(MyFirebaseInstanceIDService.this, token);
                    RegisterPushResponse response = StoreItemService.registerToReceivePushNotification(ProfileCache.getToken(MyFirebaseInstanceIDService.this), token, "android");
                    ALog.d(TAG, "sendRegistrationToServer, response=" + response);
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
                return null;
            }
        };

        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.HONEYCOMB) {
            task.executeOnExecutor(AsyncTask.THREAD_POOL_EXECUTOR);
        } else {
            task.execute();
        }
    }
}
